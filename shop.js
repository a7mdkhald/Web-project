function show1() {
    var menu = document.getElementsByClassName('men-sub')[0]
    if (menu.style.display == 'none') {
        menu.style.display = 'block';
        document.getElementById('men-btn').style.backgroundColor = 'black';
    } else {
        menu.style.display = 'none';
        document.getElementById('men-btn').style.backgroundColor = 'black';
    }
}
function hide1() {
    var menu = document.getElementsByClassName('men-sub')[0]
    if (menu.style.display == 'block') {
        menu.style.display = 'none';
    } else {
            menu.style.display = 'none';
    }
    document.getElementById('men-btn').style.backgroundColor = 'transparent';
}
